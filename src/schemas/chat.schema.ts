import mongoose from "mongoose";
import { ChatInterface } from "../types";

const chatSchema = new mongoose.Schema(
  {
    participants: [
      {
        type: mongoose.Schema.Types.ObjectId,
        ref: "User",
      },
    ],
    messages: [
      {
        type: mongoose.Schema.Types.ObjectId,
        ref: "Message",
        default: [],
      },
    ],
  },
  { timestamps: true }
);

const Chat = mongoose.model<ChatInterface>("Chat", chatSchema);
export default Chat;
